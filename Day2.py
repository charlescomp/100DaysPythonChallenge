# Understanding Data Types and Manipulating Strings

# Integer
my_int = 10
print(f"Integer: {my_int}")

# Float
my_float = 20.5
print(f"Float: {my_float}")

# String
my_string = "Hello, World!"
print(f"String: {my_string}")

# Boolean
my_bool = True
print(f"Boolean: {my_bool}")

# Manipulating Strings
name = "Alice"
greeting = "Hello, " + name + "!"
print(greeting)

# Using f-strings for string formatting
age = 25
intro = f"My name is {name} and I am {age} years old."
print(intro)

# String methods
upper_case = my_string.upper()
print(f"Upper case: {upper_case}")

lower_case = my_string.lower()
print(f"Lower case: {lower_case}")

title_case = my_string.title()
print(f"Title case: {title_case}")

# Accessing string characters
first_char = my_string[0]
print(f"First character: {first_char}")

last_char = my_string[-1]
print(f"Last character: {last_char}")
